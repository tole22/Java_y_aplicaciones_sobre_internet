package calcu.model;

public class Calculadora {

	public int suma(int top, int bottom){
		return top+bottom;
	}
	public int resta(int top, int bottom){
		return top-bottom;
	}
	public int producto(int top, int bottom){
		return top*bottom;
	}
	public int division(int top, int bottom){
		return top/bottom;
	}
}
